package binarytree;

import org.w3c.dom.Node;

/**
 *
 * @author migue
 */
public class clsNode {
    int value;
    Node left;
    Node right;

    clsNode (int value) {
        this.value = value;
        right = null;
        left = null;
    }

    public int getValue() {
        return value;
    }

    public void setValue(int value) {
        this.value = value;
    }

    public Node getLeft() {
        return left;
    }

    public void setLeft(Node left) {
        this.left = left;
    }

    public Node getRight() {
        return right;
    }

    public void setRight(Node right) {
        this.right = right;
    }
    

}
